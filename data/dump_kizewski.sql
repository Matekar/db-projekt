CREATE OR REPLACE FUNCTION projekt.redaktorzy_artykulu(id_artykul integer)
 RETURNS TABLE(id integer, imie_redaktor character varying, nazwisko_redaktor character varying, rola character varying)
 LANGUAGE sql
AS $function$
	SELECT r.id_redaktor, r.imie_redaktor, r.nazwisko_redaktor, rra.rola FROM projekt.relacja_redaktor_artykul rra
	JOIN projekt.redaktorzy r ON rra.id_red = r.id_redaktor
	WHERE rra.id_art = id_artykul
$function$

CREATE OR REPLACE FUNCTION projekt.autorzy_artykulu(id_artykul integer)
 RETURNS TABLE(id integer, imie character varying, nazwisko character varying)
 LANGUAGE sql
AS $function$
	SELECT a.id_author, a.imie, a.nazwisko FROM projekt.relacja_autor_artykul raa
	JOIN projekt.autorzy a ON raa.id_aut = a.id_author
	WHERE raa.id_art = id_artykul
$function$
